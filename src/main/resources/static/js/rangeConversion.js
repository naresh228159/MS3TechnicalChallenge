function showConversionTable(formValues){
	 $('table').hide();
     $('#searchBlock').hide();
    $.get('/convert/range?' + formValues , function(results){
    	$('#conversionResults').empty();
    	console.log('Results '+results);
    	$.each(results, function(key,value) {
        	$('<tr><td>'+key+'</td><td id="'+key+'">'+value+'</td><tr>').appendTo('#conversionResults');
        });
        $('table').show();
        $('#searchBlock').show();
    });
}

function showFilteredResults() {
	  // Declare variables
	  var input, filter, table, tr, td, i;
	  input = document.getElementById("myInput");
	  filter = input.value.toUpperCase();
	  table = document.getElementById("conversionTable");
	  tr = table.getElementsByTagName("tr");

	  // Loop through all table rows, and hide those who don't match the search query
	  for (i = 0; i < tr.length; i++) {
	    td = tr[i].getElementsByTagName("td")[0];
	    if (td) {
	      if (td.innerHTML.toUpperCase().indexOf(filter) > -1) {
	        tr[i].style.display = "";
	      } else {
	        tr[i].style.display = "none";
	      }
	    }
	  }
	}

